# Tipe data

#integer : angka tanpa koma
data_integer = 20
print ("data : ", data_integer, type(data_integer))

#float : angka dengan koma
data_float = 20.5
print ("data : ", data_float, type(data_float))

#boolean : logika true/false
data_boolean = True
print ("data : ", data_boolean, type(data_boolean))

#tipe data khusus
#complex : bilangan kompleks di matematika
data_complex = (4,6)
print ("data : ", data_complex, type(data_complex))