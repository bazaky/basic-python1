# Operasi Aritmatika
print('\nProgram Aritmatika\n')

a = 8
b = 3

# Penjumlahan
hasil = a + b
print(a,'+',b,'=',hasil)

# Pengurangan
hasil = a - b
print(a,'-',b,'=',hasil)

# Perkalian
hasil = a * b
print(a,'*',b,'=',hasil)

# Pembagian
hasil = a / b
print(a,'/',b,'=',hasil)

# eksponen (pangkat)
hasil = a ** b
print(a,'**',b,'=',hasil)

# modulus
hasil = a % b
print(a,'%',b,'=',hasil)

# floor division
hasil = a // b
print(a,'//',b,'=',hasil)